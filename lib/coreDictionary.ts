function buildQuery(...parts: string[]) {
  return parts.join(" ").replace(/\s+/g, " ").trim();
}

function createQueries(
  subject: string,
  focusA: string,
  focusB: string,
  focusC: string,
  focusD: string
) {
  return [
    buildQuery(subject, focusA, "aesthetic"),
    buildQuery(subject, focusB, "cinematic"),
    buildQuery(subject, focusC, "moody"),
    buildQuery(subject, focusA, "film"),
    buildQuery(subject, focusD, "night"),
    buildQuery(subject, focusB, "street photography"),
    buildQuery(subject, focusC, "cinematic night"),
    buildQuery(subject, focusD, "moody film"),
    buildQuery(subject, focusA, "aesthetic night")
  ];
}

